type EventORM struct {
	AggregateId   string `gorm:"type:uuid"`
	AggregateKind int32
	CreatedAt     *time.Time
	Data          string `gorm:"type:jsonb"`
	EventCode     int32
	Id            string `gorm:"type:uuid;primary_key"`
	PublishedAt   *time.Time
	SideAffect    bool
	TransactionId string `gorm:"type:uuid"`
}

// TableName overrides the default tablename generated by GORM
func (EventORM) TableName() string {
	return "events"
}

// ToORM runs the BeforeToORM hook if present, converts the fields of this
// object to ORM format, runs the AfterToORM hook, then returns the ORM object
func (m *Event) ToORM(ctx context.Context) (EventORM, error) {
	to := EventORM{}
	var err error
	if prehook, ok := interface{}(m).(EventWithBeforeToORM); ok {
		if err = prehook.BeforeToORM(ctx, &to); err != nil {
			return to, err
		}
	}
	to.Id = m.Id
	to.AggregateId = m.AggregateId
	to.TransactionId = m.TransactionId
	to.Data = m.Data
	if m.CreatedAt != nil {
		t := m.CreatedAt.AsTime()
		to.CreatedAt = &t
	}
	if m.PublishedAt != nil {
		t := m.PublishedAt.AsTime()
		to.PublishedAt = &t
	}
	to.AggregateKind = int32(m.AggregateKind)
	to.EventCode = int32(m.EventCode)
	to.SideAffect = m.SideAffect
	if posthook, ok := interface{}(m).(EventWithAfterToORM); ok {
		err = posthook.AfterToORM(ctx, &to)
	}
	return to, err
}

