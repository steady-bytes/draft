// Create ...
func (m *EventStoreDefaultServer) Create(ctx context.Context, in *CreateEventRequest) (*CreateEventResponse, error) {
	db := m.DB
	if custom, ok := interface{}(in).(EventStoreEventWithBeforeCreate); ok {
		var err error
		if db, err = custom.BeforeCreate(ctx, db); err != nil {
			return nil, err
		}
	}
	res, err := DefaultCreateEvent(ctx, in.GetPayload(), db)
	if err != nil {
		return nil, err
	}
	out := &CreateEventResponse{Result: res}
	if custom, ok := interface{}(in).(EventStoreEventWithAfterCreate); ok {
		var err error
		if err = custom.AfterCreate(ctx, out, db); err != nil {
			return nil, err
		}
	}
	return out, nil
}

