// DefaultPatchSetProcess executes a bulk gorm update call with patch behavior
func DefaultPatchSetProcess(ctx context.Context, objects []*Process, updateMasks []*field_mask.FieldMask, db *gorm.DB) ([]*Process, error) {
	if len(objects) != len(updateMasks) {
		return nil, fmt.Errorf(errors.BadRepeatedFieldMaskTpl, len(updateMasks), len(objects))
	}

	results := make([]*Process, 0, len(objects))
	for i, patcher := range objects {
		pbResponse, err := DefaultPatchProcess(ctx, patcher, updateMasks[i], db)
		if err != nil {
			return nil, err
		}

		results = append(results, pbResponse)
	}

	return results, nil
}

// DefaultApplyFieldMaskProcess patches an pbObject with patcher according to a field mask.
func DefaultApplyFieldMaskProcess(ctx context.Context, patchee *Process, patcher *Process, updateMask *field_mask.FieldMask, prefix string, db *gorm.DB) (*Process, error) {
	if patcher == nil {
		return nil, nil
	} else if patchee == nil {
		return nil, errors.NilArgumentError
	}
	var err error
	var updatedJoinedTime bool
	var updatedLeftTime bool
	for i, f := range updateMask.Paths {
		if f == prefix+"Id" {
			patchee.Id = patcher.Id
			continue
		}
		if f == prefix+"Name" {
			patchee.Name = patcher.Name
			continue
		}
		if f == prefix+"Group" {
			patchee.Group = patcher.Group
			continue
		}
		if f == prefix+"Local" {
			patchee.Local = patcher.Local
			continue
		}
		if f == prefix+"IpAddress" {
			patchee.IpAddress = patcher.IpAddress
			continue
		}
		if f == prefix+"ProcessKind" {
			patchee.ProcessKind = patcher.ProcessKind
			continue
		}
		if f == prefix+"Tags" {
			patchee.Tags = patcher.Tags
			continue
		}
		if !updatedJoinedTime && strings.HasPrefix(f, prefix+"JoinedTime.") {
			if patcher.JoinedTime == nil {
				patchee.JoinedTime = nil
				continue
			}
			if patchee.JoinedTime == nil {
				patchee.JoinedTime = &timestamppb.Timestamp{}
			}
			childMask := &field_mask.FieldMask{}
			for j := i; j < len(updateMask.Paths); j++ {
				if trimPath := strings.TrimPrefix(updateMask.Paths[j], prefix+"JoinedTime."); trimPath != updateMask.Paths[j] {
					childMask.Paths = append(childMask.Paths, trimPath)
				}
			}
			if err := gorm1.MergeWithMask(patcher.JoinedTime, patchee.JoinedTime, childMask); err != nil {
				return nil, nil
			}
		}
		if f == prefix+"JoinedTime" {
			updatedJoinedTime = true
			patchee.JoinedTime = patcher.JoinedTime
			continue
		}
		if !updatedLeftTime && strings.HasPrefix(f, prefix+"LeftTime.") {
			if patcher.LeftTime == nil {
				patchee.LeftTime = nil
				continue
			}
			if patchee.LeftTime == nil {
				patchee.LeftTime = &timestamppb.Timestamp{}
			}
			childMask := &field_mask.FieldMask{}
			for j := i; j < len(updateMask.Paths); j++ {
				if trimPath := strings.TrimPrefix(updateMask.Paths[j], prefix+"LeftTime."); trimPath != updateMask.Paths[j] {
					childMask.Paths = append(childMask.Paths, trimPath)
				}
			}
			if err := gorm1.MergeWithMask(patcher.LeftTime, patchee.LeftTime, childMask); err != nil {
				return nil, nil
			}
		}
		if f == prefix+"LeftTime" {
			updatedLeftTime = true
			patchee.LeftTime = patcher.LeftTime
			continue
		}
		if f == prefix+"Version" {
			patchee.Version = patcher.Version
			continue
		}
		if f == prefix+"RunningState" {
			patchee.RunningState = patcher.RunningState
			continue
		}
		if f == prefix+"ProcessHealth" {
			patchee.ProcessHealth = patcher.ProcessHealth
			continue
		}
	}
	if err != nil {
		return nil, err
	}
	return patchee, nil
}

// DefaultListProcess executes a gorm list call
func DefaultListProcess(ctx context.Context, db *gorm.DB) ([]*Process, error) {
	in := Process{}
	ormObj, err := in.ToORM(ctx)
	if err != nil {
		return nil, err
	}
	if hook, ok := interface{}(&ormObj).(ProcessORMWithBeforeListApplyQuery); ok {
		if db, err = hook.BeforeListApplyQuery(ctx, db); err != nil {
			return nil, err
		}
	}
	db, err = gorm1.ApplyCollectionOperators(ctx, db, &ProcessORM{}, &Process{}, nil, nil, nil, nil)
	if err != nil {
		return nil, err
	}
	if hook, ok := interface{}(&ormObj).(ProcessORMWithBeforeListFind); ok {
		if db, err = hook.BeforeListFind(ctx, db); err != nil {
			return nil, err
		}
	}
	db = db.Where(&ormObj)
	db = db.Order("id")
	ormResponse := []ProcessORM{}
	if err := db.Find(&ormResponse).Error; err != nil {
		return nil, err
	}
	if hook, ok := interface{}(&ormObj).(ProcessORMWithAfterListFind); ok {
		if err = hook.AfterListFind(ctx, db, &ormResponse); err != nil {
			return nil, err
		}
	}
	pbResponse := []*Process{}
	for _, responseEntry := range ormResponse {
		temp, err := responseEntry.ToPB(ctx)
		if err != nil {
			return nil, err
		}
		pbResponse = append(pbResponse, &temp)
	}
	return pbResponse, nil
}

