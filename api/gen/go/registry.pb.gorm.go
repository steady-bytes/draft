type ProcessORM struct {
	Group         string
	Id            string `gorm:"type:uuid;primary_key"`
	IpAddress     string
	JoinedTime    *time.Time `gorm:"type:timestamp"`
	LeftTime      *time.Time `gorm:"type:timestamp"`
	Local         string
	Name          string
	ProcessHealth int32
	ProcessKind   int32
	RunningState  int32
	Tags          []*MetadataORM `gorm:"foreignkey:ProcessId;association_foreignkey:Id"`
	Version       string
}

// TableName overrides the default tablename generated by GORM
func (ProcessORM) TableName() string {
	return "processes"
}

// ToORM runs the BeforeToORM hook if present, converts the fields of this
// object to ORM format, runs the AfterToORM hook, then returns the ORM object
func (m *Process) ToORM(ctx context.Context) (ProcessORM, error) {
	to := ProcessORM{}
	var err error
	if prehook, ok := interface{}(m).(ProcessWithBeforeToORM); ok {
		if err = prehook.BeforeToORM(ctx, &to); err != nil {
			return to, err
		}
	}
	to.Id = m.Id
	to.Name = m.Name
	to.Group = m.Group
	to.Local = m.Local
	to.IpAddress = m.IpAddress
	to.ProcessKind = int32(m.ProcessKind)
	for _, v := range m.Tags {
		if v != nil {
			if tempTags, cErr := v.ToORM(ctx); cErr == nil {
				to.Tags = append(to.Tags, &tempTags)
			} else {
				return to, cErr
			}
		} else {
			to.Tags = append(to.Tags, nil)
		}
	}
	if m.JoinedTime != nil {
		t := m.JoinedTime.AsTime()
		to.JoinedTime = &t
	}
	if m.LeftTime != nil {
		t := m.LeftTime.AsTime()
		to.LeftTime = &t
	}
	to.Version = m.Version
	to.RunningState = int32(m.RunningState)
	to.ProcessHealth = int32(m.ProcessHealth)
	if posthook, ok := interface{}(m).(ProcessWithAfterToORM); ok {
		err = posthook.AfterToORM(ctx, &to)
	}
	return to, err
}

